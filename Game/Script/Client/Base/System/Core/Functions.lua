function io.exists(path)
    local file = io.open(path, "r")
    if file then
        io.close(file)
        return true
    end
    return false
end

function io.readfile(path)
    local file = io.open(path, "r")
    if file then
        local content = file:read("*a")
        io.close(file)
        return content
    end
    return nil
end

function io.writefile(path, content, mode)
    mode = mode or "w+b"
    local file = io.open(path, mode)
    if file then
        if file:write(content) == nil then return false end
        io.close(file)
        return true
    else
        return false
    end
end

function io.pathinfo(path)
    local pos = string.len(path)
    local extpos = pos + 1
    while pos > 0 do
        local b = string.byte(path, pos)
        if b == 46 then -- 46 = char "."
            extpos = pos
        elseif b == 47 then -- 47 = char "/"
            break
        end
        pos = pos - 1
    end

    local dirname = string.sub(path, 1, pos)
    local filename = string.sub(path, pos + 1)
    extpos = extpos - pos
    local basename = string.sub(filename, 1, extpos - 1)
    local extname = string.sub(filename, extpos)
    return {
        dirname = dirname,
        filename = filename,
        basename = basename,
        extname = extname
    }
end

function io.filesize(path)
    local size = false
    local file = io.open(path, "r")
    if file then
        local current = file:seek()
        size = file:seek("end")
        file:seek("set", current)
        io.close(file)
    end
    return size
end

--以服务器的时区格式化时间
os.localDate = os.date
function os.date(format, time)
    if not format then
        format = "%c"
    end
    if not time then
        time = os.localTime()
    end


    time = time + (os.timeZone() + Cache.serverTimeCache:getCurTimeZone()) * 3600
    return os.localDate(format, time)
end

--本地时区
local timeZone
function os.timeZone()
    if not timeZone then
        local time = os.localTime()
        timeZone = os.difftime(time, os.localTime(os.localDate("!*t", time))) / 3600
    end
    return timeZone
end

--以服务器的时区格式化时间
os.localTime = os.time
function os.time(timeTb)
    if timeTb == nil then
        return os.localTime()
    else
        local time = os.localTime(timeTb)
        local date = os.localDate("*t", time)
        local dst = 0
        if date.isdst == true then
            dst = 1
        end
        return time + (math.floor(os.timeZone()) + Cache.serverTimeCache:getCurTimeZone() + dst) * 3600
    end
end

